#!/usr/bin/env node
/**
 * Generates lib/permissions/generated.ts from leo-api permission-matrix.ts (INV-WEB-PERM-1).
 */
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(__dirname, "..");
const matrixPath = join(
  repoRoot,
  "../leo-api/src/modules/identity/constants/permission-matrix.ts",
);
const outputPath = join(repoRoot, "lib/permissions/generated.ts");

const source = readFileSync(matrixPath, "utf8");

const matrixMatch = source.match(
  /export const ROLE_PERMISSION_MATRIX[^=]*=\s*\{([\s\S]*?)\n\};/,
);
if (!matrixMatch) {
  console.error("Could not parse ROLE_PERMISSION_MATRIX from", matrixPath);
  process.exit(1);
}

const matrixBody = matrixMatch[1];
const roleBlocks = [...matrixBody.matchAll(/(\w+):\s*allPermissions\(\[([\s\S]*?)\]\)/g)];

if (roleBlocks.length === 0) {
  console.error("No role blocks found in permission matrix");
  process.exit(1);
}

const matrix = {};
const permissionSet = new Set();

for (const [, role, permissionsBlock] of roleBlocks) {
  const permissions = [...permissionsBlock.matchAll(/'([^']+)'/g)].map((m) => m[1]);
  matrix[role] = permissions;
  for (const p of permissions) permissionSet.add(p);
}

const permissions = [...permissionSet].sort();
const roles = Object.keys(matrix).sort();

const matrixEntries = roles
  .map((role) => {
    const perms = matrix[role].map((p) => `    "${p}",`).join("\n");
    return `  ${role}: [\n${perms}\n  ] as const,`;
  })
  .join("\n");

const permissionUnion = permissions.map((p) => `  | "${p}"`).join("\n");

const output = `// AUTO-GENERATED by scripts/codegen-permissions.mjs — do not edit.
// Source: ../leo-api/src/modules/identity/constants/permission-matrix.ts

export type Permission =
${permissionUnion};

export type CanonicalRole =
${roles.map((r) => `  | "${r}"`).join("\n")};

export const ROLE_PERMISSION_MATRIX: Record<
  CanonicalRole,
  readonly Permission[]
> = {
${matrixEntries}
};

export function hasPermission(
  role: string,
  permission: Permission,
): boolean {
  const grants = ROLE_PERMISSION_MATRIX[role as CanonicalRole];
  if (!grants) return false;
  return grants.includes(permission);
}
`;

writeFileSync(outputPath, output);
console.log(`Wrote ${outputPath} (${roles.length} roles, ${permissions.length} permissions)`);
